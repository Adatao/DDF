class Summary(object):
    def __init__(self, jsummary):
        self._jsummary = jsummary
    def min(self):
        return self._jsummary.min()
            
    def max(self):
        return self._jsummary.max()
    
    def variance():
        return self._jsummary.variance()
        
    def mean():
        return self._jsummary.mean()
